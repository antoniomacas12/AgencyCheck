// ─── Utilities ───────────────────────────────────────────────────────────────

export function getAgencyBySlug(slug: string): VerifiedAgency | undefined {
  return VERIFIED_AGENCIES.find((a) => a.slug === slug);
}

export function getAgenciesBySector(sector: AgencySector): VerifiedAgency[] {
  return VERIFIED_AGENCIES.filter((a) => a.agencyType === sector);
}

export function getAgenciesByCity(city: string): VerifiedAgency[] {
  const slug = city.toLowerCase().replace(/\s+/g, "-");
  return VERIFIED_AGENCIES.filter((a) => a.supportedCities.includes(slug));
}

export function getAgenciesWithAccommodation(): VerifiedAgency[] {
  return VERIFIED_AGENCIES.filter(
    (a) => a.accommodation !== "not_provided" && a.accommodation !== "unknown"
  );
}
