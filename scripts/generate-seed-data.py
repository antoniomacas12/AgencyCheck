canonical_count = sum(1 for l in OUT_CANONICAL.read_text().splitlines() if l.strip() == "},")
mock_count      = sum(1 for l in OUT_MOCK.read_text().splitlines() if l.strip() == "},")
print(f"\nValidation:")
print(f"  canonicalAgencies.ts: {canonical_count} entries")
print(f"  mockData.ts:          {mock_count} entries")
if canonical_count != len(agencies):
    print(f"  ⚠️  Expected {len(agencies)}, got {canonical_count}")
else:
    print(f"  ✓ Entry count matches canonical ({len(agencies)})")
